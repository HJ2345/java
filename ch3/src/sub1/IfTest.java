public class IfTest {
	
	public static void main(String[] args) {
   int n1=1,n2=2,n3=3,n4=2;
   if(n1>n2) {
	   System.out.println("n1 은 n2보다 크다.");
   }else if (n2>n3) {
	   System.out.println("n2 는 n3보다 크다");
}
   else if (n3>n4) {
		System.out.println("n3는 n4보다 크다");
}
   else {
	   System.out.println("n4가 가장 크다");
   }
		
		
		
	}

}
